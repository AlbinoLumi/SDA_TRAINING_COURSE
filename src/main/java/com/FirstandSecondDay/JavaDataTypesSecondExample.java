package com.FirstandSecondDay;

public class JavaDataTypesSecondExample {

    public static void  main(String[] args){
       final byte mySecondByteVariable = 3;
        short mySecondShortVariable = 5;
        int mySecondIntVariable = 6;
        long mySecondLongVariable = 8l;
        float mySecondFloatVariable = 10f;
       final  double mySecondDoubleVariable= 12d;

        System.out.println(mySecondByteVariable);
        System.out.println(mySecondShortVariable);
        System.out.println(mySecondIntVariable);
        System.out.println(mySecondLongVariable);
        System.out.println(mySecondFloatVariable);
        System.out.println(mySecondDoubleVariable);



    }
}
