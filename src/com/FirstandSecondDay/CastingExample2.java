package com.FirstandSecondDay;

public class CastingExample2 {

    public static void main(String[] args) {

        float vleraFloat = 2.8f;
        float vleraFloat2 = 3.8f;
        float vleraFloat3 = 4.2f;
        char character1 = 'a';
        char character2 = 'b';
        char character3 = 'c';

        int vlera1 = (int) vleraFloat;
        int vlera2 = (int) vleraFloat2;
        int vlera3 = (int) vleraFloat3;
        System.out.println(vlera1);
        System.out.println(vlera2);
        System.out.println(vlera3);

        int char1 = (int) character1;
        int char2 = (int) character2;
        int char3 = (int) character3;
        System.out.println(char1);
        System.out.println(char2);
        System.out.println(char3);


    }
}





